// Connect to MetaMask
async function connect() {
    if (window.ethereum) {
        await window.ethereum.request({ method: 'eth_requestAccounts' })
        window.web3 = new Web3(window.ethereum)
        console.log('Connected to MetaMask!')
    } else {
        alert('Please install MetaMask to use this feature.')
    }
}

